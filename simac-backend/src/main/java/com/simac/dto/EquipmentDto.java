package com.simac.dto;

public class EquipmentDto {
    private String id_equipment;
    private String name_equipment;
    private String number_plate;
    private String brand_equipment;
    private String model_equipment;
    private String chassis_equipment;
    private String service_equipment;

    public String getId_equipment() {
        return id_equipment;
    }

    public void setId_equipment(String id_equipment) {
        this.id_equipment = id_equipment;
    }

    public String getName_equipment() {
        return name_equipment;
    }

    public void setName_equipment(String name_equipment) {
        this.name_equipment = name_equipment;
    }

    public String getNumber_plate() {
        return number_plate;
    }

    public void setNumber_plate(String number_plate) {
        this.number_plate = number_plate;
    }

    public String getBrand_equipment() {
        return brand_equipment;
    }

    public void setBrand_equipment(String brand_equipment) {
        this.brand_equipment = brand_equipment;
    }

    public String getModel_equipment() {
        return model_equipment;
    }

    public void setModel_equipment(String model_equipment) {
        this.model_equipment = model_equipment;
    }

    public String getChassis_equipment() {
        return chassis_equipment;
    }

    public void setChassis_equipment(String chassis_equipment) {
        this.chassis_equipment = chassis_equipment;
    }

    public String getService_equipment() {
        return service_equipment;
    }

    public void setService_equipment(String service_equipment) {
        this.service_equipment = service_equipment;
    }
}
